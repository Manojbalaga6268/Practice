package com.switchcases;

public class switchexample1 {
	public static void main(String[]args) {
		int a = 100;
		switch(a) {
		case 10:
		System.out.println("it is 10");
		break;
		case 20:
		System.out.println("it is 20");	
		break;
		case 100:
		System.out.println("it is 100");	
		break;
		case 30:
		System.out.println("it is 30");	
		break;
		case 101:
		System.out.println("it is 101");	
		break;
		}
	}

}
